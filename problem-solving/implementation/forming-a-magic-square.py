def formingMagicSquare(s):
    s = sum(s, [])  # convert 2d to 1d

    """
        magic squares sum to 15
        5 has to be in center
        2,4,6,8 at corners
        1,3,7,9 middle
    """
    possible_magic_squares = [
        [8, 1, 6, 3, 5, 7, 4, 9, 2],
        [6, 1, 8, 7, 5, 3, 2, 9, 4],
        [4, 9, 2, 3, 5, 7, 8, 1, 6],
        [2, 9, 4, 7, 5, 3, 6, 1, 8],
        [8, 3, 4, 1, 5, 9, 6, 7, 2],
        [4, 3, 8, 9, 5, 1, 2, 7, 6],
        [6, 7, 2, 1, 5, 9, 8, 3, 4],
        [2, 7, 6, 9, 5, 1, 4, 3, 8]]

    mincost = sys.maxsize

    for mag in possible_magic_squares:
        diff = 0
        for i, j in zip(s, mag):
            diff += abs(i-j)
            print(i, j, diff)
        mincost = min(mincost, diff)
    return mincost


